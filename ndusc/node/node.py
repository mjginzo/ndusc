"""Node class module."""


# Node ------------------------------------------------------------------------
class Node(dict):
    """Node class."""

    # __init__ ----------------------------------------------------------------
    def __init__(self, *arg, **kw):
        """Initialization."""
        super(Node, self).__init__(*arg, **kw)
    # ----------------------------------------------------------------------- #

    # get_file ----------------------------------------------------------------
    def get_file(self):
        """Get filename."""
        return self['model']['file']
    # ----------------------------------------------------------------------- #

    # get_function ------------------------------------------------------------
    def get_function(self):
        """Get function name."""
        return self['model']['function']
    # ----------------------------------------------------------------------- #
# --------------------------------------------------------------------------- #
